public class Personas {

    String nombre;
    String apellidop;
    String apellidom;
    Integer fecha;
    String sexo;
    String entidad;

    public Personas(String nombre,String apellidop, String apellidom, Integer fecha, String sexo, String entidad){
        this.nombre = nombre;
        this.apellidop = apellidop;
        this.apellidom = apellidom;
        this.fecha = fecha;
        this.sexo = sexo;
        this.entidad = entidad;
    }
}
